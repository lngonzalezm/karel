package Ejercicio2;

import becker.robots.Thing;

/**
 *
 * @author Laura Gonzalez
 */
public class Segmentos {
   private Thing cosa1;
   private Thing cosa2;
   private Thing cosa3;

    public Segmentos(Thing cosa1, Thing cosa2, Thing cosa3) {
        this.cosa1 = cosa1;
        this.cosa2 = cosa2;
        this.cosa3 = cosa3;
    }

    public Thing getCosa1() {
        return cosa1;
    }

    public void setCosa1(Thing cosa1) {
        this.cosa1 = cosa1;
    }

    public Thing getCosa2() {
        return cosa2;
    }

    public void setCosa2(Thing cosa2) {
        this.cosa2 = cosa2;
    }

    public Thing getCosa3() {
        return cosa3;
    }

    public void setCosa3(Thing cosa3) {
        this.cosa3 = cosa3;
    }
  
   
    
   
   
}
